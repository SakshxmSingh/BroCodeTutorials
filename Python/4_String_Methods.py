# String methods = allows to do operations on strings

name = "saksham"

print(len(name))                    # len() returns the length of the object
print(name.find("s"))               # find() returns the index of the object in given string, starts with 0
print(name.capitalize())            # capitalize() capitalises the string
print(name.upper())                 # upper() upper-cases the subject
print(name.lower())                 # lower() lower-cases the subject
print(name.isdigit())               # returns True or False if the string is a digit
print(name.isalpha())               # returns True or False if the string is all alphabets
print(name.count("a"))              # returns count of subject in string
print(name.replace("s", "f"))       # returns the string with replacing stuff with other stuff
print(name*5)                       # returns the string *'n' times

