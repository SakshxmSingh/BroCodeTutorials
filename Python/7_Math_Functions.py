import math

pi = -3.14
x = 1
y = 2
z = 3

print(round(pi))                                # round() rounds to the nearest integer
print(math.ceil(pi))                            # ceil() rounds to next int
print(math.floor(pi))                           # floor() rounds to last int
print(abs(pi))                                  # gives absolute value
print(pow(pi, 2))                               # pow( , ) raises base to given exponent
print(math.sqrt(pi*pi))                         # square root
print(max(pi, x, y, z))                         # returns the max of all
print(min(pi, x, y, z))                         # returns the min of all
